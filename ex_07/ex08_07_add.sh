
#!/usr/bin/env bash
add() {
  # предполагаем целые числа (можно расширить на float через bc)
  if ! [[ "$1" =~ ^-?[0-9]+$ && "$2" =~ ^-?[0-9]+$ ]]; then
    echo "Оба аргумента должны быть целыми числами."
    return 1
  fi
  echo $(( $1 + $2 ))
}

if [ $# -eq 2 ]; then
  add "$1" "$2"
else
  read -p "Аргумент 1: " a
  read -p "Аргумент 2: " b
  add "$a" "$b"
fi
